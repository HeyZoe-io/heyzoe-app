export type DashboardSettingsT = (typeof dashboardSettingsI18n)[DashboardLang];

export function dashboardSettingsT(lang: DashboardLang): DashboardSettingsT {
  return dashboardSettingsI18n[lang];
}

export function formatConcurrentEditorNames(names: string[], t: DashboardSettingsT): string {
  const cleaned = names.map((n) => n.trim()).filter(Boolean);
  if (!cleaned.length) return t.otherUser;
  if (cleaned.length === 1) return cleaned[0]!;
  if (cleaned.length === 2) return t.concurrentNamesAnd(cleaned[0]!, cleaned[1]!);
  return t.concurrentNamesList(cleaned.slice(0, -1).join(", "), cleaned[cleaned.length - 1]!);
}

export function settingsStepHref(base: string, step: number, lang: DashboardLang): string {
  const q = new URLSearchParams({ step: String(step) });
  if (lang === "en") q.set("lang", "en");
  return `${base}?${q.toString()}`;
}
