type Turn = { role?: string; content?: string };

async function tryLogLpLandingTurn(sessionId: string, userMessage: string, assistantMessage: string) {
  const sid = sessionId.trim().slice(0, 200) || "anon";
  try {
    const supabase = createSupabaseAdminClient();
    const { error } = await supabase.from("lp_zoe_landing_chat_turns").insert({
      session_id: sid,
      user_message: userMessage.slice(0, 12_000),
      assistant_message: assistantMessage.slice(0, 12_000),
    });
    if (error) console.error("lp_zoe_landing log:", error.message);
  } catch (e) {
    console.error("lp_zoe_landing log failed", e);
  }
}

export async function POST(req: NextRequest) {
  try {
    const apiKey = resolveClaudeApiKey();
    if (!apiKey) {
      return NextResponse.json({ error: "Missing ANTHROPIC_API_KEY" }, { status: 500 });
    }

    const body = (await req.json()) as { message?: string; history?: Turn[]; sessionId?: string };
    const { message, history, sessionId: rawSid } = body;
    if (!message?.trim()) {
      return NextResponse.json({ error: "No message" }, { status: 400 });
    }

    const client = new Anthropic({ apiKey });
    const messages: Anthropic.MessageParam[] = [];

    if (Array.isArray(history)) {
      for (const turn of history.slice(-6)) {
        const role = turn?.role === "assistant" ? "assistant" : turn?.role === "user" ? "user" : null;
        const content = String(turn?.content ?? "").trim();
        if (!role || !content) continue;
        messages.push({ role, content });
      }
    }

    messages.push({ role: "user", content: message.trim() });

    const response = await client.messages.create({
      model: CLAUDE_CHAT_MODEL,
      max_tokens: 320,
      system: buildZoeSystemPrompt(new Date()),
      messages,
    });

    const answer =
      Array.isArray(response.content) && response.content[0]?.type === "text"
        ? response.content[0].text
        : "אופס, משהו השתבש 😅";

    void tryLogLpLandingTurn(
      typeof rawSid === "string" ? rawSid : "anon",
      message.trim(),
      answer
    );

    return NextResponse.json({ answer });
  } catch (error) {
    console.error("Zoe bot error:", error);
    return NextResponse.json({ answer: "אופס, נתקעתי רגע. נסו שוב 😅" });
  }
}
